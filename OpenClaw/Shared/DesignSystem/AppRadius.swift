import SwiftUI

/// Consistent corner radius tokens.
enum AppRadius {
    /// 8 pt — small elements (stat cells, pills, badges)
    static let sm: CGFloat = 8
    /// 10 pt — form inputs
    static let md: CGFloat = 10
    /// 12 pt — buttons
    static let lg: CGFloat = 12
    /// 16 pt — cards
    static let card: CGFloat = 16
}
